def compPoints?(a,b)
  return (a[0] == b[0] && a[1] == b[1])
end

class GridPoint
  def initialize(y,x,snake)
    @y = y
    @x = x
    @snake = snake
    @tailWasHere=false
  end

  def setOrigin()
    @start = true;
  end

  # def enter()
  #   puts "ENTERING <#{@x},#{@y}>"
  #   @snake.moveTo(@x,@y)
  # end
  def printPoint()
    headAt = @snake.headPos
    tailAt = @snake.tailPos
    if @start
      print "s"
    elsif compPoints?(headAt, [@y,@x])
      print "H"
    elsif compPoints?(tailAt, [@y,@x])
      print "T"
    elsif tailNowAt()
      print "t"
    elsif @tailWasHere
      print "#"
    else
      print "."
    end
  end
  def tailNowAt()
    # puts "is tail covering <#{@y},#{@x}>"
    # puts "\t#{@snake.getTail()}"
    @snake.getTail().each do |knot|

      return true if compPoints?([@y,@x], knot)
    end
    return false
  end

  def tailWasHere()
    @tailWasHere = true
  end
  def tailWasHere?()
    return @tailWasHere
  end
end

class Snake
  def initialize()
    @headAt = [0,0]
    @tailAt = [0,0]
    @startAt = [0,0]
    @tail = []
    (1..10).each do |k|
      @tail.push([0,0])
    end
  end

  def moveTo(y,x,dir)
    puts "moveTo(<#{y}>,<#{x}> because <#{dir}>)" if $DEBUG
    @headAt = [y,x]
    unless isTailClose?()
      #talk needs to get in line like a good tail
      case dir
      when "R"
        @tailAt = [@headAt[0],@headAt[1]-1]
      when "L"
        @tailAt = [@headAt[0],@headAt[1]+1]
      when "U"
        @tailAt = [@headAt[0]-1,@headAt[1]]
      when "D"
        @tailAt = [@headAt[0]+1,@headAt[1]]
      else
        raise "BAD DIR"
      end
      @tail.shift
      @tail.append(@tailAt)
      $grid[@tailAt[0]][@tailAt[1]].tailWasHere()
    end
  end
  def isTailClose?()
    puts "head is at: <#{@headAt[0]},#{@headAt[1]}>" if $DEBUG
    puts "tail is at: <#{@tailAt[0]},#{@tailAt[1]}>" if $DEBUG
    found = false
    ((@headAt[0]-1)..(@headAt[0]+1)).each do |y|
      ((@headAt[1]-1)..(@headAt[1]+1)).each do |x|
        puts "\tLooking for tail at <#{y},#{x}>" if $DEBUG
        if compPoints?([y,x],@tailAt)
          puts "\t\tFOUND" if $DEBUG
          found = true
          break 2
        end
      end
    end
    return found
  end
  def headPos()
    return @headAt
  end
  def tailPos()
    return @tailAt
  end
  def getTail()
    @tail
  end
end

def printGrid()
  puts `clear` unless $DEBUG
  $snake.getTail.each do |kt|
    print "#{kt}"
  end
  puts ''
  $grid.keys.reverse_each do |y|
    print "%03d" % y
    $grid[y].keys.each do |x|
      $grid[y][x].printPoint
    end
    print "\n"
  end
  sleep 0.1
end

def getVisitCount()
  visited = 0
  $grid.keys.each do |x|
    $grid[x].keys.each do |y|
      visited +=1 if $grid[x][y].tailWasHere?()
    end
  end
  return visited
end

def initGrid()
  (-($gridQuadSize)..($gridQuadSize)).each do |y|
    $grid[y] = {}
    (-($gridQuadSize)..($gridQuadSize)).each do |x|
      $grid[y][x] = GridPoint.new(y,x,$snake)
      if (x==0 && y==0)
        $grid[y][x].setOrigin()
        $grid[y][x].tailWasHere()
      end
    end
  end
end

def processCommand(dir, count)
  for steps in 1..count
    case dir
    when "R"
      $curX+=1
    when "L"
      $curX-=1
    when "U"
      $curY+=1
    when "D"
      $curY-=1
    else
      raise "BAD DIR"
    end
    $snake.moveTo($curY,$curX,dir)
  end
  printGrid() if $DEBUG
  # raise
end

