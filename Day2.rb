#!/usr/bin/ruby

# A X -> rock
# B Y -> paper
# C Z -> sizors 

$scores = {"rock" =>1, "paper" =>2, "scissors"=>3}
$themMap = {"A" => "rock", "B" => "paper", "C" => "scissors"}
$usMap = {"X" => "rock", "Y" => "paper", "Z" => "scissors"}
totalScorePt1=0
totalScorePt2=0

def roundResult(them, us) 
    res = "lose"
    resScore = 0
    if $themMap[them] == $usMap[us]
        res = "draw" 
        resScore= 3
    else
        case them
        when "A"
            res = "win" if us == "Y"
            resScore= 6 if us == "Y"
        when "B"
            res = "win" if us == "Z"
            resScore= 6 if us == "Z"
        when "C"
            res = "win" if us == "X"
            resScore= 6 if us == "X"
        end
    end

    # puts $usMap[us]
    puts "\t>> <#{res}> #{$scores[$usMap[us]]} #{resScore}"
    score = $scores[$usMap[us]] + resScore

    return [res, score]
end

def doWhat(them, command)
    mustPlay = ''
    case command
    when "X" #lose
        mustPlay = 'scissors' if them == "A" #rock
        mustPlay = 'rock' if them == "B" #paper
        mustPlay = 'paper' if them == "C" #scissors
    when "Y" #draw
        mustPlay = 'rock' if them == "A" #rock
        mustPlay = 'paper' if them == "B" #paper
        mustPlay = 'scissors' if them == "C" #scissors
    when "Z" #win
        mustPlay = 'paper' if them == "A" #rock
        mustPlay = 'scissors' if them == "B" #paper
        mustPlay = 'rock' if them == "C" #scissors
    end
    unMap = {"rock"=>"X", "paper"=>"Y", "scissors"=>"Z"}
    return unMap[mustPlay]
end

DATA.each_line do |line| 
    (them, us) = line.scan(/\w+/)
    puts "<#{them} #{us}>"
    resultPt1 = roundResult(them, us) #get score reading us as play
    puts " --> #{resultPt1[0]}, #{resultPt1[1]}"
    totalScorePt1 += resultPt1[1]

    resultPt2 = roundResult(them, doWhat(them, us)) #get score reading us as play
    puts " --> #{resultPt2[0]}, #{resultPt2[1]}"
    totalScorePt2 += resultPt2[1]
end

puts "PART 1 TOTAL SCORE --> #{totalScorePt1}"
puts "PART 2 TOTAL SCORE --> #{totalScorePt2}"

